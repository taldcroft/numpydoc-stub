"""The inner module that implements :meth:`add`.
"""


def add(a, b):
    """Adds ``a`` to ``b``.

    Parameters
    ----------
    a : any
        First argument to add.
    b : type of a
        Second argument to add.

    Returns
    -------
    type of a:
        The result of ``a + b``.
    """
    return a + b
